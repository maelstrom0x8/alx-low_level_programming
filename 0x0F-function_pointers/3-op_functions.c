#include "3-calc.h"

/**
 * op_add - A function that add two integers
 * and return the result
 * @a: first operand
 * @b: second operand
 * Return: The sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - A function that calculates the diff-
 * erences between  two integers and return the result
 * @a: first operand
 * @b: second operand
 * Return: The difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - A function that multiplies two integers
 * and return the result
 * @a: first operand
 * @b: second operand
 * Return: The product
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - A function that divides an integer by
 * another and return the result
 * @a: first operand
 * @b: second operand
 * Return: The quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - A function that calculates the modulo of
 * an  integer by another and return the result
 * @a: first operand
 * @b: second operand
 * Return: The remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
